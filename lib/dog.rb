class Dog
  # This writes the name of the dog to an instance variable @name
  def name=(dog_name)
    @name = dog_name
  end

  # This reads the name of the dog from an instance variable @name
  def name
    @name
  end

  # This writes the breed of the dog from an instance variable @breed
  def breed=(dog_breed)
    @breed = dog_breed
  end

  # This reads the breed of the dog from an instance variable @breed
  def breed
    @breed
  end

end
